
public abstract class interpreter {
	private group g;
	private musician m;
	private album[] a;
	private song[] s;
	
	public group getGroup(){
		return g;
	}
	
	public musician getMusician(){
		return m;
	}
	
	public album[] getAlbum(){
		return a;
	}
	
	public song[] getSong(){
		return s;
	}
	

}
